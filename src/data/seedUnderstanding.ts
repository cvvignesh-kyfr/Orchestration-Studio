export interface EntitySpec {
  id: string;
  name: string;
  mode: "Authored" | "External" | "Runtime";
  type: "Merchant" | "Category" | "UserAccount" | "Transaction" | "Artifact" | "SystemContext";
  aliases?: string[];
  canonicalId?: string;
  relationships?: string[];
  authoredValues?: string[];
  externalConfig?: {
    sourceProvider: string;
    cardinality: "High" | "Medium";
    resolutionContract: {
      inputs: string[];
      outputs: string[];
    };
    confidencePolicy: {
      autoResolveAbove: number;
      askClarificationBelow: number;
      proceedWithDisclosureRange: string;
    };
    fallbackPolicy: {
      noMatch: string;
      multipleMatches: string;
      providerDown: string;
    };
    exposedFields: string[];
  };
  runtimeDerivationContext?: string;
  bindings: {
    intents: string[];
    journeys: string[];
    tools: string[];
    artifacts: string[];
    contracts: string[];
  };
  status: "Draft" | "Review" | "Published" | "Archived";
  version: string;
  owner: string;
  tags: string[];
  versionHistory?: { version: string; timestamp: string; author: string; data: any }[];
}

